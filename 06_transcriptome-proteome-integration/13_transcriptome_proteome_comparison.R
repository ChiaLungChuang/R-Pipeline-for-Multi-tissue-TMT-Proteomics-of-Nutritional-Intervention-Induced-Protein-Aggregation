  ggsave(file.path(OUTPUT_DIR, "transcriptome_vs_proteome_all_tissues.pdf"),
         plot = p_combined, width = 22, height = 14, dpi = 600)
  cat("Saved: transcriptome_vs_proteome_all_tissues.pdf\n")
  
  # ---- Clean version (no labels, no R2) ----
  p_combined_clean <- ggplot(all_facet_data %>% arrange(sig_group),
                             aes(x = rna_fc, y = prot_fc)) +
    facet_grid(Comparison ~ Tissue, scales = "free", switch = "y") +
    geom_hline(yintercept = 0, linetype = "dashed",
               color = "grey60", linewidth = 0.3) +
    geom_vline(xintercept = 0, linetype = "dashed",
