  "RNA-seq only"     = "#2166AC",  # Blue
  "Proteomics only"  = "#D6604D",  # Orange-red
  "Both"             = "#762A83"   # Purple
)

sig_alphas <- c(
  "Neither"          = 0.25,
  "RNA-seq only"     = 0.55,
  "Proteomics only"  = 0.55,
  "Both"             = 0.85
)

sig_sizes <- c(
  "Neither"          = 0.8,
  "RNA-seq only"     = 1.2,
  "Proteomics only"  = 1.2,
  "Both"             = 2.0
)

# --- Comparison definitions ---
comparisons <- list(
  soluble = list(
