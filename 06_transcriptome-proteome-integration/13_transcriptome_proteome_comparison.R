      strip.text.y     = element_text(size = 9,  face = "bold", angle = 0),
      strip.placement  = "outside",
      axis.title.x     = element_text(size = 10, face = "bold"),
      axis.text        = element_text(size = 8),
      legend.position  = "bottom",
      legend.title     = element_text(size = 9,  face = "bold"),
      legend.text      = element_text(size = 8),
      panel.grid.minor = element_blank(),
      panel.spacing    = unit(0.8, "lines")
    )
  
  # ---- Annotated version (R2 + gene labels) ----
  p_combined <- ggplot(all_facet_data %>% arrange(sig_group),
                       aes(x = rna_fc, y = prot_fc)) +
    facet_grid(Comparison ~ Tissue, scales = "free", switch = "y") +
    geom_hline(yintercept = 0, linetype = "dashed",
               color = "grey60", linewidth = 0.3) +
    geom_vline(xintercept = 0, linetype = "dashed",
               color = "grey60", linewidth = 0.3) +
    geom_point(aes(color = sig_group,
                   alpha = sig_group,
                   size  = sig_group)) +
    scale_color_manual(values = sig_colors, name = "Significance") +
    scale_alpha_manual(values = sig_alphas, name = "Significance") +
    scale_size_manual( values = sig_sizes,  name = "Significance") +
    geom_text(data        = r2_labels,
              aes(x = x_pos, y = y_pos, label = label),
              inherit.aes = FALSE,
              hjust = 1, vjust = 1,
              size = 2.2, color = "black") +
    geom_text_repel(
      data          = label_data,
      aes(label     = GN),
      color         = "#762A83",
      size          = 1.8,
      fontface      = "bold",
      max.overlaps  = 12,
      box.padding   = 0.3,
      point.padding = 0.1,
      segment.color = "grey50",
      segment.size  = 0.25,
      na.rm         = TRUE
    ) +
    labs(
      title    = paste0("Transcriptome vs. Proteome: ",
                        COND_A, " vs. ", COND_B, " across five tissues"),
      subtitle = paste0("Blue = RNA-seq only  |  Orange-red = Proteomics only  ",
                        "|  Purple = Both significant\n",
                        "Top 10 purple genes labeled per panel"),
      x        = paste0("Log2FC mRNA (", COND_A, "/", COND_B, ")"),
      y        = NULL,
      caption  = paste0(
        "Significance thresholds: RNA-seq sig == 1  |  Proteomics p <= 0.05\n",
        "Log2FC values represent ", COND_A, "/", COND_B,
        " (positive = increased in ", COND_A, ")\n",
        "R2 shown per panel (all matched genes)  |  Both = significant in both datasets"
      )
    ) +
