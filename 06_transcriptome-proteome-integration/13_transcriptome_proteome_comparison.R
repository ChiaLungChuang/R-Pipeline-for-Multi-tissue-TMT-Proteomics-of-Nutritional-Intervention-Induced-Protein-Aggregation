# ==================== CORE PLOT FUNCTION ====================

make_comparison_plot <- function(merged, comp, tissue_label, tissue_color) {
  
  # ---- R2 on all matched genes ----
  fit    <- lm(prot_fc ~ rna_fc, data = merged)
  r2_all <- round(summary(fit)$r.squared, 3)
  n_all  <- formatC(nrow(merged), format = "d", big.mark = ",")
  n_both <- sum(merged$sig_group == "Both")
  
  # ---- Top 10 "Both" genes to label (ranked by combined significance) ----
  label_df <- merged %>%
    filter(sig_group == "Both") %>%
    mutate(combined_sig = (-log10(rna_p  + 1e-300)) +
             (-log10(prot_p + 1e-300))) %>%
    arrange(desc(combined_sig)) %>%
    slice_head(n = 10)
  
  merged <- merged %>%
    mutate(label = ifelse(GN %in% label_df$GN, GN, NA_character_))
  
  # ---- Category ordering so purple is drawn on top ----
  merged <- merged %>%
    mutate(sig_group = factor(sig_group,
                              levels = c("Neither", "RNA-seq only",
                                         "Proteomics only", "Both")))
  
  # ---- Axis limits with 5% padding ----
  x_range <- range(merged$rna_fc,  na.rm = TRUE)
  y_range <- range(merged$prot_fc, na.rm = TRUE)
  x_lims  <- x_range + diff(x_range) * c(-0.05, 0.05)
  y_lims  <- y_range + diff(y_range) * c(-0.05, 0.05)
  
  # ---- Build plot ----
  p <- ggplot(merged %>% arrange(sig_group),
              aes(x = rna_fc, y = prot_fc)) +
    geom_hline(yintercept = 0, linetype = "dashed",
               color = "grey60", linewidth = 0.35) +
    geom_vline(xintercept = 0, linetype = "dashed",
               color = "grey60", linewidth = 0.35) +
    geom_point(aes(color = sig_group,
                   alpha = sig_group,
                   size  = sig_group)) +
    scale_color_manual(values = sig_colors, name = "Significance") +
    scale_alpha_manual(values = sig_alphas, name = "Significance") +
    scale_size_manual( values = sig_sizes,  name = "Significance") +
    geom_text_repel(
      aes(label = label),
      color         = "#762A83",
      size          = 3,
      fontface      = "bold",
      max.overlaps  = Inf,
      box.padding   = 0.4,
      point.padding = 0.2,
      segment.color = "grey50",
      segment.size  = 0.3,
      na.rm         = TRUE
    ) +
    scale_x_continuous(labels = label_number(accuracy = 0.1),
                       limits = x_lims) +
    scale_y_continuous(labels = label_number(accuracy = 0.1),
                       limits = y_lims) +
    labs(
      title    = paste0(tissue_label, ": mRNA vs. ",
                        comp$title_kw,
                        " (", COND_A, " vs. ", COND_B, ")"),
      subtitle = paste0("Blue = RNA-seq only  |  Orange-red = Proteomics only  ",
                        "|  Purple = Both significant"),
      x        = paste0("Log2FC mRNA (", COND_A, "/", COND_B, ")"),
      y        = comp$y_label,
      caption  = paste0(
        "Significance thresholds: RNA-seq sig == 1  |  Proteomics p <= 0.05\n",
        "Top 10 purple genes labeled by combined significance (-log10 p RNA-seq + proteomics)\n",
        "Log2FC values represent ", COND_A, "/", COND_B,
        " (positive = increased in ", COND_A, ")\n",
        "R2 = ", r2_all,
        " (all matched genes, n = ", n_all, ")",
        "  |  Both significant: n = ", n_both
      )
    ) +
    theme_minimal(base_size = 12) +
    theme(
      plot.title       = element_text(size = 14, face = "bold"),
      plot.subtitle    = element_text(size = 10, color = "grey40"),
      plot.caption     = element_text(size = 9,  color = "grey30", hjust = 0),
      axis.title       = element_text(size = 12, face = "bold"),
      axis.text        = element_text(size = 11),
      legend.title     = element_text(size = 11, face = "bold"),
      legend.text      = element_text(size = 10),
      legend.position  = "right",
      panel.grid.minor = element_blank()
    )
  
  return(p)
}

# ==================== MAIN LOOP ====================

# Storage for combined facet data
facet_data_list <- list()

for (tiss_id in names(tissue_map)) {
  
  tinfo        <- tissue_map[[tiss_id]]
  tissue_label <- tinfo$label
  tissue_file  <- tinfo$file
  tissue_color <- tissue_colors[[tissue_label]]
  
  cat("========================================\n")
  cat("Processing:", tissue_label, "\n")
  cat("========================================\n")
  
  # ---- Load proteomics ----
  prot_file <- file.path(PROTEOMICS_DIR,
                         paste0(tissue_file, "_processed.csv"))
  
  if (!file.exists(prot_file)) {
    cat("  WARNING: Proteomics file not found:", prot_file, "\n\n")
    next
  }
  
  prot_data <- read.csv(prot_file) %>%
    group_by(GN) %>%
    slice_min(order_by = p_value_ratio, n = 1, with_ties = FALSE) %>%
    ungroup() %>%
    filter(!is.na(GN), GN != "", GN != "NA") %>%
    filter(!grepl("CON_|^co$", GN, ignore.case = TRUE))
  
  cat("  Proteomics genes:", nrow(prot_data), "\n")
  
  # ---- Extract RNA-seq columns for this tissue ----
  # Column name pattern (as read by read.csv, spaces converted to dots):
  #   Log2.Ratio...{ID}.{COND_A}.vs.{ID}.{COND_B}.
  #   P.value..{ID}.{COND_A}.vs.{ID}.{COND_B}.
  #   sig..{ID}.{COND_A}.vs.{ID}.{COND_B}.
  rna_fc_col  <- paste0("Log2.Ratio...", tiss_id, ".", COND_A,
                        ".vs.", tiss_id, ".", COND_B, ".")
  rna_p_col   <- paste0("P.value..",     tiss_id, ".", COND_A,
                        ".vs.", tiss_id, ".", COND_B, ".")
  rna_sig_col <- paste0("sig..",         tiss_id, ".", COND_A,
                        ".vs.", tiss_id, ".", COND_B, ".")
  
  missing_cols <- setdiff(c(rna_fc_col, rna_p_col, rna_sig_col),
                          names(rnaseq_raw))
  if (length(missing_cols) > 0) {
    cat("  WARNING: Missing RNA-seq columns:",
        paste(missing_cols, collapse = ", "), "\n\n")
    next
  }
  
  rna_data <- rnaseq_raw %>%
    select(Symbol,
           rna_fc  = all_of(rna_fc_col),
           rna_sig = all_of(rna_sig_col),
           rna_p   = all_of(rna_p_col)) %>%
    filter(!is.na(Symbol), Symbol != "") %>%
    mutate(rna_fc  = as.numeric(rna_fc),
           rna_sig = as.integer(rna_sig),
           rna_p   = as.numeric(rna_p))
  
  cat("  RNA-seq genes:", nrow(rna_data), "\n")
  
  # ---- Loop over three comparisons ----
  for (comp_name in names(comparisons)) {
    
    comp <- comparisons[[comp_name]]
    cat("  Comparison:", comp_name, "...\n")
    
    # ---- Merge by gene name (RNA-seq Symbol <-> proteomics GN) ----
    merged <- inner_join(
      rna_data,
      prot_data %>%
        select(GN,
               prot_fc = all_of(comp$prot_fc),
               prot_p  = all_of(comp$prot_p)),
      by = c("Symbol" = "GN")
    ) %>%
      rename(GN = Symbol) %>%
      filter(!is.na(rna_fc), !is.na(prot_fc),
             !is.na(rna_p),  !is.na(prot_p)) %>%
      mutate(
        sig_rna   = rna_sig == 1,
        sig_prot  = prot_p  <= 0.05,
        sig_group = case_when(
          sig_rna  & sig_prot  ~ "Both",
          sig_rna  & !sig_prot ~ "RNA-seq only",
          !sig_rna & sig_prot  ~ "Proteomics only",
          TRUE                 ~ "Neither"
        ),
        Tissue     = tissue_label,
        Comparison = comp_name
      )
    
    n_matched <- nrow(merged)
    cat("    Matched genes:", n_matched, "\n")
    cat("    Both significant:", sum(merged$sig_group == "Both"), "\n")
    
    if (n_matched < 5) {
      cat("    WARNING: Too few matched genes, skipping.\n")
      next
    }
    
    # ---- Store for combined facet plot ----
    facet_data_list[[paste(tiss_id, comp_name, sep = "_")]] <- merged
    
    # ---- Export detailed matched gene table ----
    prot_full <- prot_data %>%
      select(
        GN,
        Protein_Accession  = Protein.Accession,
        Description,
        Log2FC_soluble,   p_value_soluble,
        Log2FC_insoluble, p_value_insoluble,
        Log2FC_ratio,     p_value_ratio
      )
    
    export_df <- inner_join(
      rnaseq_raw %>%
        select(
          GN          = Symbol,
          mRNA_Log2FC = all_of(rna_fc_col),
          mRNA_pvalue = all_of(rna_p_col),
          mRNA_sig    = all_of(rna_sig_col)
        ),
      prot_full,
      by = "GN"
    ) %>%
      filter(GN %in% merged$GN) %>%
      mutate(
        Tissue     = tissue_label,
        Comparison = comp_name,
        sig_group  = case_when(
          mRNA_sig == 1 & !!sym(paste0("p_value_", comp_name)) <= 0.05 ~ "Both",
          mRNA_sig == 1 & !!sym(paste0("p_value_", comp_name)) >  0.05 ~ "RNA-seq only",
          mRNA_sig != 1 & !!sym(paste0("p_value_", comp_name)) <= 0.05 ~ "Proteomics only",
          TRUE ~ "Neither"
        )
      ) %>%
      select(
        Tissue, Comparison, GN,
        Protein_Accession, Description,
        mRNA_Log2FC, mRNA_pvalue, mRNA_sig,
        Log2FC_soluble,   p_value_soluble,
        Log2FC_insoluble, p_value_insoluble,
        Log2FC_ratio,     p_value_ratio,
        sig_group
      ) %>%
      arrange(sig_group, GN)
    
    export_file <- paste0("matched_genes_",
                          gsub(" ", "_", tissue_label),
                          "_", comp_name, ".csv")
    write.csv(export_df, file.path(OUTPUT_DIR, export_file),
              row.names = FALSE)
    cat("    Exported:", export_file,
        "(", nrow(export_df), "genes )\n")
    
    # ---- Save individual plot ----
    plot_file <- file.path(OUTPUT_DIR,
                           paste0("transcriptome_vs_", comp_name, "_",
                                  gsub(" ", "_", tissue_label), ".pdf"))
    p <- make_comparison_plot(merged, comp, tissue_label, tissue_color)
    ggsave(plot_file, plot = p, width = 10, height = 8, dpi = 600)
    cat("    Saved:", plot_file, "\n")
  }
  
  cat("\n")
}

