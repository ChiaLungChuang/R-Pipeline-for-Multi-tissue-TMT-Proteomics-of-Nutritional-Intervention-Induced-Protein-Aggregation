# ==============================================================================
# TRANSCRIPTOME vs. PROTEOME COMPARISON ACROSS MULTIPLE TISSUES
# Integrates RNA-seq log2FC with three proteomics metrics per tissue:
#   (1) Soluble protein fraction
#   (2) Insoluble protein fraction
#   (3) Insoluble/Soluble ratio (protein aggregation index)
#
# Outputs:
#   - 15 individual PDFs (5 tissues x 3 comparisons)
#   - 1 combined facet PDF (all tissues, annotated with R2 and gene labels)
#   - 1 combined facet PDF (all tissues, clean)
#   - 15 matched gene CSVs (one per tissue x comparison)
#   - 1 R2 summary CSV
#
# Expected folder structure:
#   project_root/
#   |-- transcriptome_proteome_comparison.R   <- this script
#   |-- data/
#   |   |-- rnaseq_data.csv                   <- RNA-seq data
#   |   +-- proteomics/                       <- proteomics CSVs folder
#   |       |-- Tissue1_processed.csv
#   |       |-- Tissue2_processed.csv
#   |       |-- Tissue3_processed.csv
#   |       |-- Tissue4_processed.csv
#   |       +-- Tissue5_processed.csv
#   +-- output/                               <- all plots and CSVs saved here
#
# Proteomics CSVs are generated by the companion processing script.
#
# INPUT DATA FORMAT:
#   RNA-seq CSV columns (per tissue):
#     Log2.Ratio...{ID}.CondA.vs.{ID}.CondB.   <- log2 fold change
#     P.value..{ID}.CondA.vs.{ID}.CondB.        <- p-value
#     sig..{ID}.CondA.vs.{ID}.CondB.            <- significance flag (0/1)
#
#   Proteomics CSV columns (required):
#     GN                  <- gene name (used for matching to RNA-seq Symbol)
#     Protein.Accession   <- protein accession
#     Description         <- protein description
#     Log2FC_soluble      <- log2FC soluble fraction (CondA/CondB)
#     p_value_soluble
#     Log2FC_insoluble    <- log2FC insoluble fraction (CondA/CondB)
#     p_value_insoluble
#     Log2FC_ratio        <- log2FC insoluble/soluble ratio (CondA/CondB)
#     p_value_ratio
# ==============================================================================

