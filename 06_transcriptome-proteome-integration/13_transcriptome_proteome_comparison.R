    prot_p   = "p_value_soluble",
    y_label  = paste0("Log2FC Soluble protein (", COND_A, "/", COND_B, ")"),
    title_kw = "Soluble protein"
  ),
  insoluble = list(
    prot_fc  = "Log2FC_insoluble",
    prot_p   = "p_value_insoluble",
    y_label  = paste0("Log2FC Insoluble protein (", COND_A, "/", COND_B, ")"),
    title_kw = "Insoluble protein"
  ),
  ratio = list(
    prot_fc  = "Log2FC_ratio",
    prot_p   = "p_value_ratio",
    y_label  = paste0("Log2FC Insoluble/Soluble ratio (", COND_A, "/", COND_B, ")"),
